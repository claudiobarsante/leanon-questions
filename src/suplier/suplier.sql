    S.SUPPLIER_NAME AS SUPPLIER,
    P.PRICE
FROM
    SUPPLIER S
INNER JOIN
    SUPPLY SU ON S.SUPPLIER_CODE = SU.SUPPLIER_CODE
INNER JOIN
    PART P ON SU.PART_CODE = P.PART_CODE
INNER JOIN
    CAR C ON SU.CAR_CODE = C.CAR_CODE
WHERE
    S.CITY = 'VITORIA'
    AND P.NAME_PART = 'MOTOR'
    AND C.NAME_CAR = 'KOMBI';

