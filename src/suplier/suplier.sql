-- SUPPLY
CREATE TABLE SUPPLY (
    ID INTEGER PRIMAR KEY,
    SUPPLIER_CODE INTEGER,
    PART_CODE INTEGER,
    CAR_CODE INTEGER,
    FOREIGN KEY (SUPPLIER_CODE) REFERENCES SUPPLIER(SUPPLIER_CODE),
    FOREIGN KEY (PART_CODE) REFERENCES PART(PART_CODE),
    FOREIGN KEY (CAR_CODE) REFERENCES CAR(CAR_CODE)
);

